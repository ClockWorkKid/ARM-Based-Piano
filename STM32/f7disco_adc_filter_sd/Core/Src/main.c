void tochar(char str[], int num){
	int i, rem, len = 0, n;

	n = num;
	while(n!=0){
		len++;
		n /= 10;
	}
	for (i = 0; i<len; i++){
		rem = num % 10;
		num /= 10;
		str[len - (i+1)] = rem + '0';
	}
	str[len] = '\r';
	str[len+1] = '\n';
	str[len+2] = 0;
}

